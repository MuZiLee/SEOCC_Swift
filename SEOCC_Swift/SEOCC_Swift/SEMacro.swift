import Foundation
import UIKit




func kBounds() -> CGRect {
    return UIScreen.main.bounds
}

func kHeight() -> CGFont {
    return UIScreen.main.bounds.height as! CGFont
}
func kWidth() -> CGFont {
    return UIScreen.main.bounds.width  as! CGFont
}
